#include <stdio.h>

int main(){
    // Atribuição de valores
    int numero1 = 5;
    int numero2 = 10;
    int numero3 = 3;
    int numero4 = 10;

    // Soma  =    5    +    10   +    3    +   10
    int soma = numero1 + numero2 + numero3 + numero4;

    // Saída de resultados
    printf("A soma dos três números é: %d\n", soma);

    return 0;
}    